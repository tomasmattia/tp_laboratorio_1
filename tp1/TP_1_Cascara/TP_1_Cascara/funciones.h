#ifndef FUNCIONES_H_INCLUDED
#define FUNCIONES_H_INCLUDED
/** \brief Suma dos parametros
 *
 * \param parametro A = numero entero
 * \param parametro B = numero entero
 * \return devuelve la suma de ambos
 *
 */
float suma(float,float);
/** \brief resta dos parametros
 *
 * \param parametro A = numero entero
 * \param parametro B = numero entero
 * \return devuelve la resta del primero por el segundo
 *
 */
float resta(float,float);
/** \brief divide dos parametros
 *
 * \param parametro A = numero entero
 * \param parametro B = numero entero
 * \return devuelve la division del primero por el segundo
 *
 */
float division(float,float);
/** \brief multiplica dos parametros
 *
 * \param parametro A = numero entero
 * \param parametro B = numero entero
 * \return devuelve la multiplicacion de ambos
 *
 */
float multiplicacion(float,float);
/** \brief saca el factorial de un parametro
 *
 * \param parametro A = numero entero
 * \return devuelve el factorial de A
 *
 */
float factorial(float);

#endif // FUNCIONES_H_INCLUDED
